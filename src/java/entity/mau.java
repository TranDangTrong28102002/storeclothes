public class mau {
    private String idMau;
    private String color;

    public mau() {
    }

    public mau(String idMau, String color) {
        this.idMau = idMau;
        this.color = color;
    }

    public String getIdMau() {
        return idMau;
    }

    public void setIdMau(String idMau) {
        this.idMau = idMau;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
    
}
